# include<stdio.h>

// Program to show division of integer number with a float number.

int main()
{
    int a;
    printf("Enter an Integer Value : ");
    scanf("%d",&a);
    
    float b;
    printf("Enter a Float Value : ");
    scanf("%f",&b);
    
    printf("Division of INT value by Float value is : %f", a/b);
    
    return 0;
}
